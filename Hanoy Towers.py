def hanoi(n, source, target, auxiliary):
    if n > 0:
        # Шаг 1: Переместить (n-1) дисков с исходного стержня на вспомогательный
        hanoi(n-1, source, auxiliary, target)

        # Шаг 2: Переместить оставшийся диск с исходного стержня на целевой
        print(f"Переместите диск {n} с {source} на {target}")

        # Шаг 3: Переместить (n-1) дисков с вспомогательного стержня на целевой
        hanoi(n-1, auxiliary, target, source)

# Пример использования функции для 3 дисков
hanoi(15, 'A', 'C', 'B')